package mx.uv.fei.Logic;

public class Student extends User{
    
    int idStudent;
    int idEnrollmentType;
    int idUser;

    public int getIdStudent() {
        return idStudent;
    }

    public void setIdStudent(int idStudent) {
        this.idStudent = idStudent;
    }

    public int getIdEnrollmentType() {
        return idEnrollmentType;
    }

    public void setIdEnrollmentType(int idEnrollmentType) {
        this.idEnrollmentType = idEnrollmentType;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

}
