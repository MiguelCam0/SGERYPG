package mx.uv.fei.Logic;

public class PreProject {
    
    private int id;
    private String nameReceptionWork;
    private String Title;
    private String description;
    private String researchline;
    private String place;
    private String requirements;
    private String status;
    private int LGCA;
    private int modality;

        
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNameReceptionWork() {
        return nameReceptionWork;
    }

    public void setNameReceptionWork(String nameReceptionWork) {
        this.nameReceptionWork = nameReceptionWork;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String Title) {
        this.Title = Title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getResearchline() {
        return researchline;
    }

    public void setResearchline(String researchline) {
        this.researchline = researchline;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getRequirements() {
        return requirements;
    }

    public void setRequirements(String requirements) {
        this.requirements = requirements;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getLGCA() {
        return LGCA;
    }

    public void setLGCA(int LGCA) {
        this.LGCA = LGCA;
    }

    public int getModality() {
        return modality;
    }

    public void setModality(int modality) {
        this.modality = modality;
    }

}
